package com.billcoreatech.daycnt311.dayManager;

public class DayinfoBean {
    int id ;
    String mDate ;
    String msg ;

    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setMdate(String mDate) {
        this.mDate = mDate;
    }

    public String getMdate() {
        return mDate;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public String getMsg() {
        return msg;
    }
}
